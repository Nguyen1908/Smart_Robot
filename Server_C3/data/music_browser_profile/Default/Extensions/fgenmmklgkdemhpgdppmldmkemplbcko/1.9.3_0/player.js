var overlayElem = document.getElementById('overlay');
var playWrapperElem = document.getElementById('play-wrapper');
var openOptionsElem = document.getElementById('open-options');

var autoplayForOrigin = false;
var urlParams = new URLSearchParams(window.location.search);
if(urlParams.has('autoplay') && urlParams.get('autoplay') != 0) {
    autoplayForOrigin = true;
}
if (autoplayForOrigin) {
    overlayElem.remove();
    // Start playback call is at end of script
}

var startPlayback = () => {
    var scriptElem = document.createElement('script');
    scriptElem.src = '/ruffle/ruffle.js';
    scriptElem.onload = () => {
      window.parent.postMessage({action: "playbackRequested"}, '*');  
    };
    document.body.appendChild(scriptElem);
}

var windowFetch = window.fetch;
var latestFetchId = 1;
var fetchResponses = {};
var processFetchDataResponse = (fetchId, fetchResponse) => {
  // fetchResponse here is the base64 string
  // secondResponse is a proper Response object
  // This workaround is needed because the extension messaging cannot pass Response objects
  // from bg to extension page, therefore we re-create it here.
  fetch(fetchResponse).then((secondResponse) => {
    fetchResponses[fetchId](secondResponse);
    delete fetchResponses[fetchId];
  });
};

window.fetch = (fetchUrlOrRequest) => {
  var shouldIntercept = false;
  // If this is a Request object, it's in format we want and likely generated by Ruffle.
  // If it's a string, we're not interested at the moment.
  if (fetchUrlOrRequest instanceof Request) {
    try {
      var url = new URL(fetchUrlOrRequest.url);
      if (url.protocol === 'http:' || url.protocol === 'https:') {
        // Only process http(s) URLs
        shouldIntercept = true;
      }
    } catch(e) {
      // Ignore malformed URLs, let window.fetch crash and burn later
    }
  }
  if (shouldIntercept) {
    latestFetchId++;
    var fetchId = latestFetchId;
    return new Promise((resolve, reject) => {
      fetchResponses[fetchId] = resolve;
      window.parent.postMessage({action: "fetchData", fetchUrl: fetchUrlOrRequest.url, fetchId: fetchId}, '*');
    });
    
  } else {
    return windowFetch(fetchUrlOrRequest);
  }
}

var player;

window.addEventListener('message', (msg) => {
    //console.info('player.js received message:', msg);
    //console.info('player.js:', msg.origin, msg.source === window.parent);
    if (!msg.isTrusted || msg.source !== window.parent) {
        console.error('Untrusted message received.');
        return;
    }

    
    if (msg.data.action == 'loadPlayer') {
        var src = msg.data.src;
        var baseUrl = msg.data.baseUrl;

        var rewriteRules = [];
        try {
            // Rewrite relative URLs to original site base URL
            rewriteRules.push([/^chrome\-extension:\/\/[a-z]+\/external\/(.*)$/, new URL(baseUrl + "$1").href]);
        } catch(e) {}

        window.RufflePlayer.config = {
            "polyfills": false,
            "autoplay": "on",
            // "warnOnUnsupportedContent": false,
            "upgradeToHttps": true,
            "logLevel": "error", // info, debug, trace
            // Note: Setting base does not resolve CORS issues, but may help in some situations.
            // Also helps fetch interception determine the actual URL.
            "base": baseUrl,
            "splashScreen": false,
            "contextMenu": "on",
            "openUrlMode": "allow", // introduced in https://github.com/ruffle-rs/ruffle/pull/10625
            "allowNetworking": "all", // introduced in https://github.com/ruffle-rs/ruffle/pull/10625
            "allowScriptAccess": false, // part of https://github.com/ruffle-rs/ruffle/pull/10625,
            "favorFlash": false, // introduced in https://github.com/ruffle-rs/ruffle/pull/11503
            "urlRewriteRules": rewriteRules,
            "unmuteOverlay": "visible" // If autoplay is enabled, this value is hidden by default
        };

        const ruffle = window.RufflePlayer.newest();
        player = ruffle.createPlayer();
        
        const playerContainer = player.shadowRoot.querySelector('#container');

        var addContextMenuOverrideStyles = () => {
            if (player.shadowRoot.querySelector('#mk1-context-menu-styles')?.length) { return; }
            var styleElem = document.createElement('style');
            styleElem.id = 'mk1-context-menu-styles';
            // Hide some context menu items and an associated separator
            styleElem.innerHTML = `
                #context-menu-overlay li.menu-item[data-text^="Copy Debug Info"],
                #context-menu-overlay li.menu-item[data-text^="About Ruffle"],
                #context-menu-overlay li.menu-separator:has(+ li.menu-item[data-text^="About Ruffle"])
                {
                    display: none;
                }
            `;
            player.shadowRoot.appendChild(styleElem);
        }
        
        addContextMenuOverrideStyles();

        // Apply consistent branding for in-player messages
        // Not using dynamicStyles because weird things happen with existing style element
        var addOverrideStyles = () => {
            if (playerContainer.querySelector('#mk1-styles')?.length) { return; }
            var styleElem = document.createElement('style');
            styleElem.id = 'mk1-styles';
            styleElem.innerHTML = `
                :host {
                    letter-spacing: normal;
                    font-family: sans-serif;
                }
                
                #unmute-overlay { display: none !important; }
                #unmute-overlay-mk1 {
                    display: none;
                    opacity: 0;
                    
                    position: absolute;
                    width: 190px;
                    height: 30px;
                    top: auto;
                    left: auto;
                    bottom: 20px;
                    right: 0;
                    
                    cursor: pointer;
                    
                    box-shadow: 0px 0px 0px 2px #fff;
                }
                @keyframes fadeIn {
                    0% { opacity: 0; }
                    100% { opacity: 1; }
                }
                @keyframes fadeOut {
                    0% { opacity: 1; }
                    99% { opacity: 0; }
                    100% { opacity: 0; display: none; }
                }
                #unmute-overlay-mk1.show {
                    display: block;
                    animation: fadeIn 300ms;
                    animation-fill-mode: forwards;
                }
                #unmute-overlay-mk1.hide {
                    animation: fadeOut 300ms;
                }
                #unmute-overlay-mk1 .background {
                    position: absolute;
                    top: 0;
                    left: 0;
                    opacity: 0.5;
                    
                    background: #000;
                    width: 100%;
                    height: 100%;
                }
                #unmute-overlay-mk1:hover .background {
                    opacity: 0.8;
                }
                #unmute-overlay-mk1, #unmute-overlay-mk1 .background {
                    border-radius: 5px 0 0 5px;
                }
                #unmute-overlay-mk1 .icon {
                    top: 6px;
                    left: 10px;
                    max-width: none;
                    max-height: none;
                    transform: none;
                    opacity: 1;
                    
                    width: auto;
                    height: 20px;
                    position: absolute;
                }
                #unmute-overlay-mk1 .unmuteText {
                    color: #fff;
                    position: absolute;
                    font-size: 1.3em;
                    right: 10px;
                    top: 3px;
                }
            `;
            playerContainer.appendChild(styleElem);
        }
        addOverrideStyles();
        
        // Override unmute overlay (used in autoplay scenarios)
        // unmute icon from Ruffle itself, with text removed
        // Clear original overlay, since we don't use it except to observe style changes
        var originalUnmuteOverlay = player.shadowRoot.querySelector("#unmute-overlay");
        originalUnmuteOverlay.innerHTML = '';
        
        var unmuteIcon = '<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" preserveAspectRatio="xMidYMid" viewBox="0 0 512 584" width="100%" height="100%" scale="0.8"><path fill="#FFF" stroke="#FFF" d="m457.941 256 47.029-47.029c9.372-9.373 9.372-24.568 0-33.941-9.373-9.373-24.568-9.373-33.941 0l-47.029 47.029-47.029-47.029c-9.373-9.373-24.568-9.373-33.941 0-9.372 9.373-9.372 24.568 0 33.941l47.029 47.029-47.029 47.029c-9.372 9.373-9.372 24.568 0 33.941 4.686 4.687 10.827 7.03 16.97 7.03s12.284-2.343 16.971-7.029l47.029-47.03 47.029 47.029c4.687 4.687 10.828 7.03 16.971 7.03s12.284-2.343 16.971-7.029c9.372-9.373 9.372-24.568 0-33.941z"></path><path fill="#FFF" stroke="#FFF" d="m99 160h-55c-24.301 0-44 19.699-44 44v104c0 24.301 19.699 44 44 44h55c2.761 0 5-2.239 5-5v-182c0-2.761-2.239-5-5-5z"></path><path fill="#FFF" stroke="#FFF" d="m280 56h-24c-5.269 0-10.392 1.734-14.578 4.935l-103.459 79.116c-1.237.946-1.963 2.414-1.963 3.972v223.955c0 1.557.726 3.026 1.963 3.972l103.459 79.115c4.186 3.201 9.309 4.936 14.579 4.936h23.999c13.255 0 24-10.745 24-24v-352.001c0-13.255-10.745-24-24-24z"></path></svg>';
        // Create our own unmute overlay
        var unmuteOverlay = document.createElement('div');
        unmuteOverlay.id = 'unmute-overlay-mk1';
        var backgroundElem = document.createElement('div');
        backgroundElem.classList.add('background');
        unmuteOverlay.appendChild(backgroundElem);
        var iconElem = document.createElement('div');
        iconElem.classList.add('icon');
        iconElem.innerHTML = unmuteIcon;
        unmuteOverlay.appendChild(iconElem);
        var unmuteText = document.createElement('div');
        unmuteText.classList.add('unmuteText');
        unmuteText.innerText = 'Click to unmute';
        unmuteOverlay.appendChild(unmuteText);
        // Append overlay
        playerContainer.appendChild(unmuteOverlay);
        
        // Observe original overlay for style changes
        var styleObserver = new MutationObserver(function(mutations) {
          mutations.forEach(function(mutationRecord) {
              if (mutationRecord.attributeName == 'style') {
                  if (originalUnmuteOverlay.style.display == 'block') {
                      // Show overlay. Slight delay to allow everything else to render first.
                      setTimeout(() => {
                          unmuteOverlay.classList.add('show');
                      }, 500);
                  } else {
                      // Hide overlay
                      unmuteOverlay.classList.add('hide');
                      setTimeout(() => {
                          // Reset state
                          unmuteOverlay.classList.remove('show', 'hide'); 
                      }, 1000);
                  }
              }
          });    
        });

        styleObserver.observe(originalUnmuteOverlay, { 
          attributes: true, 
          attributeFilter: ['style']
        });
        
        // Override panic message
        var playerContainerChildElemChangedObserver = new MutationObserver(function(mutations) {
          mutations.forEach(function(mutationRecord) {
              if (mutationRecord.addedNodes) {
                  if (mutationRecord.addedNodes?.[0]?.id == 'panic') {
                      window.parent.postMessage({action: "showPlaybackError"}, '*');
                      // Hide panic element until we show our error message
                      var panicElem = playerContainer.querySelector('#panic');
                      panicElem.id = 'panic-disabled';
                      panicElem.innerText = '';
                  }
              }
          });    
        });
        
        playerContainerChildElemChangedObserver.observe(playerContainer, { 
          childList: true
        });

        const container = document.getElementById("player-container");
        container.appendChild(player);
        
        if (msg.data.width != 'auto' && msg.data.height != 'auto') {
            // Only set dimensions if non-auto value.
            // Auto value is set by extension when we want to use file metadata over any page dimensions
            // This is necessary for the web player at /flash-emulator/player/
            player.style.width = msg.data.width;
            player.style.height = msg.data.height;
        } else {
            player.onloadedmetadata = () => {
                player.style.width = player.metadata.width + 'px';
                player.style.height = player.metadata.height + 'px';
                window.parent.postMessage({action: "updatePlayerDimensions", width: player.metadata.width, height: player.metadata.height}, '*');
            }
        }
        
        fetch(src)
        .then(r => { return r.arrayBuffer(); })
        .then(data => {
            player.load({
                data: data,
                parameters: msg.data.flashvars
            });
            document.body.classList.add('initialized');
        });
    } else if (msg.data.action == 'fetchDataResponse') {
        processFetchDataResponse(msg.data.fetchId, msg.data.fetchResponse);
    } else if (msg.data.action == 'updateInternalPlayerDimensions' && msg.data.width && msg.data.height) {
        player.style.width = msg.data.width;
        player.style.height = msg.data.height;
    }
});

var showOptions = () => {
    window.parent.postMessage({action: "openOptions"}, '*');
}

// Ensure this logic is after all other setup logic to prevent race conditions
if (autoplayForOrigin) {
    // Overlay removal is at beginning of script
    startPlayback();
} else {
    playWrapperElem.addEventListener('click', () => {
        overlayElem.remove();
        startPlayback();
    });
    openOptionsElem.addEventListener('click', () => {
        showOptions();
    });
}